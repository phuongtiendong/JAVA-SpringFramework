package Command;

public class Controller {
    public Controller(){}

    public void turnOn(){
        System.out.println("ТВ включен");
    }

    public void turnOff(){
        System.out.println("ТВ выключен");
    }

    public void increaseSound(){
        System.out.println("Увеличить звук");
    }

    public void reduceSound(){
        System.out.println("Уменьшить звук");
    }
}
